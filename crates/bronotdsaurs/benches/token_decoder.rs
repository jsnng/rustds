fn drain_all(mut decoder: TokenDecoder<'_, bronotdsaurs::tds::decoder::stream::NoContext>) -> usize {
    let mut count = 0usize;
    loop {
        match decoder.advance() {
            None => return count,
            Some(step) => {
                count += 1;
                match step {
                    NoContextStep::EnvChange(_, next)
                    | NoContextStep::Info(_, next)
                    | NoContextStep::LoginAck(_, next)
                    | NoContextStep::ServerError(_, next)
                    | NoContextStep::ReturnStatus(_, next)
                    | NoContextStep::ReturnValue(_, next)
                    | NoContextStep::Done(_, next) => decoder = next,
                    #[cfg(feature = "tds7.4")]
                    NoContextStep::FeatureExtAck(_, next) => decoder = next,
                    NoContextStep::ContextRequired(_) | NoContextStep::Error(_) => return count,
                }
            }
        }
    }
}

fn bench_token_decoder(c: &mut Criterion) {
    // Skip the 8-byte packet header to start at the first token.
    let body = &CAPTURE[8..];

    let mut g = c.benchmark_group("token_decoder");
    g.throughput(Throughput::Bytes(body.len() as u64));
    g.bench_function("login_response", |b| {
        b.iter(|| {
            let decoder = TokenDecoder::new(black_box(body));
            drain_all(decoder)
        });
    });
    g.finish();
}

criterion_group!(benches, bench_token_decoder);
criterion_main!(benches);
